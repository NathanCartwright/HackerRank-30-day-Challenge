
	import java.util.*;

	public class Day5Loops 
	{
	    public static void main(String[] args) 
	    {
	        Scanner in = new Scanner(System.in);
	        int n = in.nextInt();
	        in.close();
	        for(int i = 1; i < 11; i++)
	        {
	            
	            System.out.printf("%d x %d = %d\n", n, i, (n * i));
	           
	            
	        }
	        
	    }
	}


